# ---------- Задача 1 ----------
# Вывести все числа от 1 до 20, которые делятся на 3

print("Задача 1:")
for i in range(1, 21):
    if i % 3 == 0:
        print(i)

# ---------- Задача 2 ----------
# Найти сумму всех чисел больше 5

print("\nЗадача 2:")
numbers = [3, 7, 2, 9, 12, 5]

total = 0
for n in numbers:
    if n > 5:
        total += n

print("Сумма чисел больше 5:", total)

# ---------- Задача 3 ----------
# Определить тип введённого числа

print("\nЗадача 3:")
num = int(input("Введите число: "))

if num > 0:
    print("Число положительное")
elif num < 0:
    print("Число отрицательное")
else:
    print("Это ноль")
